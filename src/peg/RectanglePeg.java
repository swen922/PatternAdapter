package peg;

public class RectanglePeg {

    private double width;
    private double height;

    public RectanglePeg() {
    }

    public RectanglePeg(double width, double height) {
        this.width = width;
        this.height = height;
    }

    public double getWidth() {
        return width;
    }

    public double getHeight() {
        return height;
    }
}
